<h3>Health, Nutrition, HIV / AIDS </h3>

<ul><li>
<i class="fa fa-check-circle-o"></i>	For the ﬁrst time in the country, associated risk   factors of  underweight, stunting and wasting using  logistic regression  were evaluated. <br/></li>
<li><i class="fa fa-check-circle-o"></i>	A study of the patterns of under nutrition by single month showed that maximum under nutrition is at 11-12 months and not around 24  months as shown earlier. This had important policy implications:  focus on children under 2 years of age; under 1 year for prevention and between 1-2 years for controlling under nutrition. <br/></li>
<li><i class="fa fa-check-circle-o"></i>	For the ﬁrst time in the country, randomized response technique was used to give estimate of percentage – a) truckers indulging in high risk sexual behavior and b) extent of child sexual abuse. <br/></li>
<li><i class="fa fa-check-circle-o"></i>	In impact assessments for WFP projects on ICDS food fortiﬁcation and Fortiﬁed Food for Education Program spread over several states, the ﬁndings revealed several bottlenecks and weaknesses of the implementation of the program, particularly with regard to erratic supply, frequency, coverage, quality, regularity, and quantity. <br/></li>
<li><i class="fa fa-check-circle-o"></i>	An analysis of the nutritive values of food items taken during the last 24 hours at home and in mid-day meal indicated that children were still far below the recommended dietary levels in iron and vitamin A. <br/></li>
<li><i class="fa fa-check-circle-o"></i>	Prepared a District Wise Food Insecurity Atlas of U.P. using. Small area estimates.<br/></li>
<li><i class="fa fa-check-circle-o"></i>	Micro-level (panchayat level) hunger mapping was done using Food Access Survey Tools (FAST) after adaptation to local conditions. A research paper prepared from this study suggests that 'access' part of food insecurity and the related anxiety component are measurable. The paper also suggests an improved hunger index, which can also be derived at local levels. It also suggests a 3-question module which can easily be canvassed at micro level (even by NSSO in consumption surveys).<br/></li>  
<li><i class="fa fa-check-circle-o"></i>	IASDS succeeded in convincing the funding agencies and NGOs of the necessity of carrying out a proper baseline, avoid reporting sub-group estimates with inadequate sub-group size, and derive estimates with adequate sample sizes. As a result NNMB stopped giving estimates of clinical deﬁciencies like Bitot Spots at district level. UNICEF carried out a study of estimating prevalence of Bitot Spots with a sample size of over 14000 in contrast to a sample of around 400 being used by NNMB. <br/></li>
<li><i class="fa fa-check-circle-o"></i>	The use of arbitrary cut-offs by IAP classiﬁcation for measuring under nutrition was (as shown by Prof. A. K. Nigam) resulting in to serious underestimation of severe under nutrition. This led to leaving out more than 50 percent severely under nourished children.<br/></li>
<li><i class="fa fa-check-circle-o"></i>	Conducted state of the art studies in many other important areas like KAPB on Infant and Maternal Care, Neonatal Mortality, Maternal and Infant Survival, and Adolescent Reproductive Sexual Health. Also conducted studies on anaemia - administering IFA tablets to adolescent girls. The analysis on sustainability of the effect of IFA even after its withdrawal was innovative.<br/>
</li></ul>
